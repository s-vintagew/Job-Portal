    void tabData()
    {
        try(Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/company","root","root");
                Statement st = con.createStatement();)
        {
            query="Select * from companies where username='"+username+"';";
            ResultSet rs=st.executeQuery(query);
            while(rs.next())
            {
                jTextField2.setText(rs.getString("company_id"));
                jTextField3.setText(rs.getString("username"));
                jTextField7.setText(rs.getString("Name"));
                jTextField5.setText(rs.getString("State"));
                jTextField6.setText(rs.getString("city"));
                jTextArea1.setText(rs.getString("street_address"));
                jTextField8.setText(rs.getString("contact_no"));
                jTextField9.setText(rs.getString("email"));
            }
        }
        catch(SQLException e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Technical Error");
        }
    }
