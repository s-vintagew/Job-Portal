    boolean checkBlank()
    {
        if(jTextField7.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Name cannot be null");
            return false;
        }
        if(jTextField8.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Contact No cannot be Blank");
            return false;
        }
        if(jTextField8.getText().length()!=10)
        {
            JOptionPane.showMessageDialog(null,"Phone Number should be 10 Digits");
            return false;
        }
        return true;
    }
